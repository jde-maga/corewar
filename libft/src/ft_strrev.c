#include <libft.h>

void	ft_strrev(char *str)
{
	size_t	i;
	size_t	j;
	char	buf;

	i = 0;
	j = ft_strlen(str) - 1;
	while (i < j)
	{
		buf = str[i];
		str[i] = str[j];
		str[j] = buf;
		i++;
		j--;
	}
}
